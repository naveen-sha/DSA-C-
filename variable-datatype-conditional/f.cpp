#include <iostream>
using namespace std;

int main(){
      int length;
      int breadth;
      cin >> breadth;
      cin >> length;
      int area = length * breadth;
      int perimeter = 2 * (length + breadth);
      cout<< "Area = " << area << endl;
      cout<< "Perimeter = " << perimeter << endl;
   };
   
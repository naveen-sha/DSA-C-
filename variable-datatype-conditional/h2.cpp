#include <iostream>
using namespace std;

int main(){
    long long n;
    long long m;
    cin >> n;
    cin >> m;
    cout<<n<< " + " <<m<< " = " <<n + m <<"\n\n";
    cout<<n<< " - " <<m<< " = " <<n - m <<"\n\n";
    cout<<n<< " * " <<m<< " = " <<n * m <<"\n\n";
    cout<<n<< " / " <<m<< " = " <<n / m <<"\n\n";
    cout<<n<< " % " <<m<< " = " <<n % m <<"\n\n";
    return 0;
};
